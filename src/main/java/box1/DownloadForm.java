package box1;

public class DownloadForm {
	
	private String nameFileToDownload;
	private String folderStructure;
	
	public DownloadForm() {
		
	}
	
	public String getNameFileToDownload() {
		return nameFileToDownload;
	}

	public void setNameFileToDownload(String nameFileToDownload) {
		this.nameFileToDownload = nameFileToDownload;
	}

	public String getFolderStructure() {
		return folderStructure;
	}

	public void setFolderStructure(String folderStructure) {
		this.folderStructure = folderStructure;
	}
	

}